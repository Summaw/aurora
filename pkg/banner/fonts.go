func GenerateArt(text string, fontName string) []string {
	text = strings.ToUpper(text)
	font := getFont(fontName)

	height := 6
	if fontName == "slant" {
		height = 5
	} else if fontName == "minimal" {
		height = 3
	}

	lines := make([]string, height)

	for _, char := range text {
		charArt, ok := font[char]
		if !ok {
			charArt = font[' ']
		}

		for i := 0; i < height && i < len(charArt); i++ {
			lines[i] += charArt[i]
		}
	}

	return lines
}

func getFont(name string) map[rune][]string {
	switch name {
	case "slant":
		return slantFont
	case "minimal":
		return minimalFont
	case "block":
		fallthrough
	default:
		return blockFont
	}
}

func ListFonts() []string {
	return []string{"block", "slant", "minimal"}
}
